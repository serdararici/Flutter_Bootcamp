void main() {
  // sabitler
  // final , const , Java(final), swift(let), Kotlin(val)

  int sayi = 30;
  print(sayi);
  sayi = 100;
  print(sayi);

  // Kodlamayı çalıltırdığımızda hafızada oluşur.
  final int numara = 100;
  print(numara);


  //Değişkeni tanımladığımızda hafızada oluşur.
  //Görsel nesnelerde kullanılır . Button, Text vb.
  const int sonuc = 500;
  print(sonuc);
}